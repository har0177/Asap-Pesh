	<h1>History (March of Time)</h1>
	<img src="images/history.jpg" alt="history-img" class="img img-responsive" style="width: 100%">
	<p class="justify">
		Khyber Pakhtunkhwa’s history is greatly influenced by its location. As the gateway to the subcontinent, it
		has a long, turbulent history of invasions by those who sought the riches of India. The Aryans began their
		migration into India around 2000 BC. Coming from the west, these semi-nomadic conquerors entered through
		Khyber Pakhtunkhwa and initially settled along with the Swat, Gomal, Kurram, and Kabul Rivers. Over several
		hundred years, more and more Aryans entered the subcontinent and migrated eastwards into modern-day India.
		The Persians conquered Gandhara in the 6th century BC and made it one of the provinces of the Achaemenid
		Empire. As a conquered nation, Gandhara gave tribute to the Persian kings.
		<br>
		<br>
		In 327 BC Alexander’s army invaded the Indian subcontinent. While Alexander was busy subduing the hill
		tribes of Swat and Kunar Valleys, his generals entered through the Khyber Pass. Before making his way south,
		Alexander resided at Taxila where he met a bold young man by the name of Chandragupta Maurya, who was an
		exiled prince of Magadha (in present-day eastern India). According to some accounts, Chandragupta Maurya
		decided to forge an empire in Alexander’s footsteps. Though Alexander did not stay long in the subcontinent
		and died shortly afterward, his reign had a significant impact. Greek culture influenced the culture of this
		region for a thousand years. A prominent example of this is the fusion of Indian and Greek art elements that
		became known as the Gandharan art style. After Alexander’s death, his general Seleucus claimed the Indian
		territories.
		<br>
		<br>
		<strong>
			Khyber Pakhtunkhwa Embraces Muslim Rulers
		</strong><br>
		Even though the Arabs had been in contact with the Pathans since the middle of the 7th century, it was in
		1001 AD when Mahmud of Ghazni defeated Jayapala, the last Hindu Shahi King of Gandhara. He then proceeded
		south to Punjab. His successive campaigns weakened Hindu rule not only in KP but also in the subcontinent
		and allowed for the spread of Islam.
		<br>
		<br>
		<strong>
			Under the Mughal Empire
		</strong><br>
		Babar, the founder of the Mughal Empire, first entered the subcontinent via the Khyber Pass in 1505 and took
		Kohat and Bannu before returning to Kabul. His fifth and final march into the continent began in 1525 and a
		year later he defeated the last Lodhi king before declaring himself the new Emperor of Delhi. Babar united
		many tribes in KP, including the Yusufzais, under his banner and led them to many regions in India. He won
		the hearts of Pathans over a short span of time. A detailed account of the history of KP can be found in his
		memoir, Babur-Nama. He paved the way for Mughal rule over this area till the 18th century, with the
		exception of brief rule under Sher Shah Suri.After Babar’s death, Humayun succeeded him. But a Pathan named
		Sher Shah Suri took control of the region from him. Humayun regained the throne fifteen years later and his
		son Akbar succeeded him. However, Akbar could not conquer all parts of KP.
		<br><br>
		The tribes of the Yousufzais and Khattaks were engaged in a century-long feud. Khushhal Khan Khattak formed
		an alliance with the Mughals during the reign of Emperor Shah Jahan. But the Yusufzais were opposed to the
		Mughal rule in Khyber Pakhtunkhwa. Shah Jahan appointed Khushhal Khan Khattak the guardian of the king’s
		highway to Peshawar and confirmed his position as the tribal chief. He even went to the Delhi court and
		joined them in many campaigns. The Khattaks broke ties with the Mughal Empire during Aurangzeb’s rule due to
		a conflict with a Mughal Governor over toll collection.
		<br><br>
		In the wake of Aurangzeb’s death in 1707, the empire split into competing kingdoms and Nadir Shah, a Persian
		ruler, gained control over the province with support from the Pathans. One of his renowned successors, Ahmad
		Shah Abdali, launched several campaigns from the Peshawar Valley into many places across India. Abdali’s
		reign (1747 – 1772) was temporarily interrupted when the Marathas invaded in the early 1750s, but he
		succeeded in regaining control over KP in 1761. The province remained a part of Afghanistan, which had been
		established as the first Pathan state by Ahmad Shah Abdali, till its conquest by the Sikhs in 1818.
		<br>
		<br>
		<strong>
			Sikh rule
		</strong><br>
		The Sikhs invaded Khyber Pakthunkhwa in 1818 under the command of Maharaja Ranjit Singh. They first annexed
		Dera Ismail Khan and then went on to become masters of the entire province. In 1923 the Sikhs took hold of
		Peshawar, plundered the city, and burned the Bala Hissar Fortress. They razed down numerous architectural
		monuments, including the Mahabat Khan Mosque. Tribes in Khyber Pakhtunkhwa were repeatedly assaulted by Sikh
		armies. This brought the Pathans together to fight against the Sikhs. Sayyid Ahmad Shah emerged as the
		leader of this movement against the Sikhs. He launched many attacks and even managed to snatch Peshawar from
		the Sikhs for a few months. But the unity of the Pathan tribes did not survive for very long and he was
		defeated in a battle at Balakot. The Sikhs did make some contributions such as building Khyber Pakhtunkhwa’s
		first planned city known as Haripur. Following Maharaja Ranjit Singh’s death in 1839, the Sikhs fought
		amongst themselves. Ultimately, the British East India Company took hold of both the province and the Sikh
		Durbar, which they called Company Bahadur.
		<br>
		<br>
		<strong>
			Relationship with British
		</strong><br>
		In 1849, Khyber Pakhtunkhwa came under British rule. The British East India Company led many campaigns into
		areas controlled by the Pathan tribes and secured their confidence. The early British officers played a
		pivotal role in winning the hearts of the Pathans. So strong was the relationship between the British and
		the Pathans that during the Indian Rebellion of 1857 many people in Peshawar and surrounding areas supported
		the British, while the rest of India was mutinous.
		<br>
		Till the late 19th century there was no formal border between Afghanistan and Khyber Pakhtunkhwa, which was
		then part of British India. In 1893, after two wars, Sir Mortimer Durand and AbdurRahmanKhan, the amir of
		Afghanistan, agreed upon an international border that came to be known as the Durand Line. It now separates
		the Pakistani regions of Baluchistan, Khyber Pakhtunkhwa, and FATA from Afghanistan.
		<br>
		The governor of Khyber Pakhtunkhwa, Lord Curzon, inaugurated the province at ShahiBagh in 1902, and in 1931
		it was elevated to the status of a governor-ruled province. Mirza Ali Khan, a wazir from Waziristan,
		spearheaded a civil disobedience movement against the British in 1936. The Khilafat and Hijrat movements
		also took place in the region but were not successful. Another popular freedom movement was launched by
		Abdul Ghaffar Khan, also known as Bacha Khan, and his brother Dr. Khan Sahib. It was a nonviolent movement
		and their followers were known as KhudaiKhidmatgar or Servants of God. Khyber Pakhtunkhwa was granted
		self-government in 1937.
		<br>
		<br>
		<strong>
			Post-independence
		</strong><br>
		Khyber Pakhtunkhwa decided to join Pakistan after independence. This was an attempt to develop a stronger
		relationship with Muhammad Ali Jinnah.
		<br>
		Abdul Ghaffar Khan urged the Government to give the province a name that reflects the Pathan identity. It
		had been known as northwest Frontier Province (NWFP) since its inauguration by the British. Pashtunistan was
		one of the proposed names but it did not get political support as this name had become too controversial.
		Eventually, in 2010 the province was renamed Khyber Pakhtunkhwa.
